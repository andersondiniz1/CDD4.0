package Aula01.POO.Classe4;

public class SomarMetodos {
    
    int somar(int a, int b) {
        return a + b;
    }

    int somar(int a, int b, int c) {
        return a + b + c;
    }

    double mult(double a, double b) {
        return a + b;
    }

    double mult(double a, double b, double c) {
        return a + b + c;
    }
}
